# 1. Check the data type of a variable. 

a = "Amit Kaushal"
print(a, type(a))

b = 68
print(b, type(b))

c = 9.8
print(c, type(c))

d = True
print(d, type(d))

e = [8, "Amit", False, 9.5]
e.append(34)
print(e, type(e))

f = (2, "abcd", True)
print(f, type(f))

g = {1 : "Patna", 2 : "Darbhanga", 3 : "Bihar"}
print(g, type(g))